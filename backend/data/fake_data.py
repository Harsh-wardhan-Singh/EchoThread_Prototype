def _at_day(day, hour, minute=0):
	return datetime.combine(day, datetime.min.time()).replace(hour=hour, minute=minute)


def _day_from_week(week_start, offset):
	return week_start + timedelta(days=offset)


def _entry(entry_id, email, text, day, sentiment, emotion, risk, risk_score, hour, minute):
	return {
		"id": entry_id,
		"email": email,
		"text": text,
		"created_at": _at_day(day, hour, minute).isoformat(),
		"checkin_date": day.isoformat(),
		"sentiment": sentiment,
		"emotion": emotion,
		"risk": risk,
		"risk_score": risk_score,
	}


